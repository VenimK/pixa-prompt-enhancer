/**
 * Theme Exporter/Importer - Save and load custom themes
 * Provides comprehensive theme management functionality
 */
class ThemeExporter {
    constructor(themeManager) {
        this.themeManager = themeManager;
        this.exportFormats = ['json', 'css', 'scss'];
        this.importFormats = ['json', 'css'];
    }

    exportTheme(themeId, format = 'json') {
        const theme = this.themeManager.themes.get(themeId);
        if (!theme) {
            throw new Error(`Theme '${themeId}' not found`);
        }

        switch (format.toLowerCase()) {
            case 'json':
                return this.exportAsJSON(theme);
            case 'css':
                return this.exportAsCSS(theme);
            case 'scss':
                return this.exportAsSCSS(theme);
            default:
                throw new Error(`Unsupported export format: ${format}`);
        }
    }

    exportAsJSON(theme) {
        const exportData = {
            ...theme,
            exportedAt: new Date().toISOString(),
            version: '1.0',
            format: 'json',
            metadata: {
                name: theme.name,
                description: theme.description || '',
                category: theme.category,
                baseTheme: theme.baseTheme,
                isCustom: theme.isCustom || false,
                createdAt: theme.createdAt || new Date().toISOString(),
                exportedBy: 'PiXa Prompt Enhancer Theme Editor'
            }
        };

        return {
            filename: `${this.sanitizeFilename(theme.name)}.json`,
            content: JSON.stringify(exportData, null, 2),
            mimeType: 'application/json'
        };
    }

    exportAsCSS(theme) {
        let css = `/* Theme: ${theme.name} */\n`;
        css += `/* Generated by PiXa Prompt Enhancer Theme Editor */\n`;
        css += `/* Exported: ${new Date().toISOString()} */\n\n`;

        // Add CSS custom properties
        css += `:root {\n`;
        
        if (theme.colors) {
            Object.entries(theme.colors).forEach(([key, value]) => {
                const cssVar = `--${this.kebabCase(key)}-color`;
                css += `  ${cssVar}: ${value};\n`;
            });
        }

        if (theme.fonts) {
            Object.entries(theme.fonts).forEach(([key, value]) => {
                const cssVar = `--font-${key}`;
                css += `  ${cssVar}: ${value};\n`;
            });
        }

        if (theme.spacing) {
            Object.entries(theme.spacing).forEach(([key, value]) => {
                const cssVar = `--spacing-${key}`;
                css += `  ${cssVar}: ${value};\n`;
            });
        }

        // Add custom properties
        if (theme.customProperties) {
            Object.entries(theme.customProperties).forEach(([key, value]) => {
                css += `  ${key}: ${value};\n`;
            });
        }

        css += `}\n\n`;

        // Add component styles
        if (theme.componentStyles) {
            Object.entries(theme.componentStyles).forEach(([component, styles]) => {
                css += `/* ${component} styles */\n`;
                css += styles + '\n\n';
            });
        }

        // Add custom CSS
        if (theme.customCSS) {
            css += `/* Custom CSS */\n`;
            css += theme.customCSS + '\n';
        }

        return {
            filename: `${this.sanitizeFilename(theme.name)}.css`,
            content: css,
            mimeType: 'text/css'
        };
    }

    exportAsSCSS(theme) {
        let scss = `// Theme: ${theme.name}\n`;
        scss += `// Generated by PiXa Prompt Enhancer Theme Editor\n`;
        scss += `// Exported: ${new Date().toISOString()}\n\n`;

        // Add SCSS variables
        if (theme.colors) {
            scss += `// Color variables\n`;
            Object.entries(theme.colors).forEach(([key, value]) => {
                const scssVar = `$${this.kebabCase(key)}-color`;
                scss += `${scssVar}: ${value};\n`;
            });
            scss += '\n';
        }

        if (theme.fonts) {
            scss += `// Font variables\n`;
            Object.entries(theme.fonts).forEach(([key, value]) => {
                const scssVar = `$font-${key}`;
                scss += `${scssVar}: ${value};\n`;
            });
            scss += '\n';
        }

        if (theme.spacing) {
            scss += `// Spacing variables\n`;
            Object.entries(theme.spacing).forEach(([key, value]) => {
                const scssVar = `$spacing-${key}`;
                scss += `${scssVar}: ${value};\n`;
            });
            scss += '\n';
        }

        // Add CSS custom properties mapping
        scss += `:root {\n`;
        if (theme.colors) {
            Object.entries(theme.colors).forEach(([key, value]) => {
                const cssVar = `--${this.kebabCase(key)}-color`;
                const scssVar = `$${this.kebabCase(key)}-color`;
                scss += `  ${cssVar}: ${scssVar};\n`;
            });
        }
        scss += `}\n\n`;

        // Add component styles
        if (theme.componentStyles) {
            Object.entries(theme.componentStyles).forEach(([component, styles]) => {
                scss += `// ${component} styles\n`;
                scss += styles + '\n\n';
            });
        }

        // Add custom CSS
        if (theme.customCSS) {
            scss += `// Custom CSS\n`;
            scss += theme.customCSS + '\n';
        }

        return {
            filename: `${this.sanitizeFilename(theme.name)}.scss`,
            content: scss,
            mimeType: 'text/x-scss'
        };
    }

    importTheme(content, format = 'json') {
        switch (format.toLowerCase()) {
            case 'json':
                return this.importFromJSON(content);
            case 'css':
                return this.importFromCSS(content);
            default:
                throw new Error(`Unsupported import format: ${format}`);
        }
    }

    importFromJSON(jsonContent) {
        try {
            const data = JSON.parse(jsonContent);
            
            // Validate required fields
            if (!data.name || !data.id) {
                throw new Error('Invalid theme JSON: missing required fields');
            }

            // Generate new ID if conflicts exist
            let themeId = data.id;
            if (this.themeManager.themes.has(themeId)) {
                themeId = `imported_${Date.now()}`;
            }

            const theme = {
                ...data,
                id: themeId,
                importedAt: new Date().toISOString(),
                isCustom: true
            };

            // Register the theme
            this.themeManager.registerTheme(themeId, theme);

            return theme;
        } catch (error) {
            throw new Error(`Failed to import JSON theme: ${error.message}`);
        }
    }

    importFromCSS(cssContent) {
        try {
            const theme = this.parseCSS(cssContent);
            
            // Generate theme ID and name
            const themeId = `css_import_${Date.now()}`;
            const themeName = this.extractThemeNameFromCSS(cssContent) || 'Imported CSS Theme';

            const themeData = {
                id: themeId,
                name: themeName,
                category: 'custom',
                description: 'Theme imported from CSS',
                ...theme,
                importedAt: new Date().toISOString(),
                isCustom: true
            };

            // Register the theme
            this.themeManager.registerTheme(themeId, themeData);

            return themeData;
        } catch (error) {
            throw new Error(`Failed to import CSS theme: ${error.message}`);
        }
    }

    parseCSS(cssContent) {
        const theme = {
            colors: {},
            fonts: {},
            spacing: {},
            customProperties: {},
            customCSS: ''
        };

        // Parse CSS custom properties
        const cssVarRegex = /--([a-zA-Z0-9-]+):\s*([^;]+);/g;
        let match;

        while ((match = cssVarRegex.exec(cssContent)) !== null) {
            const property = match[1];
            const value = match[2].trim();

            if (property.endsWith('-color')) {
                const colorKey = property.replace('-color', '');
                theme.colors[colorKey] = value;
            } else if (property.startsWith('font-')) {
                const fontKey = property.replace('font-', '');
                theme.fonts[fontKey] = value;
            } else if (property.startsWith('spacing-')) {
                const spacingKey = property.replace('spacing-', '');
                theme.spacing[spacingKey] = value;
            } else {
                theme.customProperties[`--${property}`] = value;
            }
        }

        // Extract custom CSS (non-variable CSS)
        const customCSS = cssContent.replace(/--[^;]+;/g, '').trim();
        theme.customCSS = customCSS;

        return theme;
    }

    extractThemeNameFromCSS(cssContent) {
        const commentMatch = cssContent.match(/\/\*\s*Theme:\s*([^\n]+)\s*\*\//);
        return commentMatch ? commentMatch[1].trim() : null;
    }

    downloadTheme(themeId, format = 'json') {
        try {
            const exported = this.exportTheme(themeId, format);
            
            // Create download link
            const blob = new Blob([exported.content], { type: exported.mimeType });
            const url = URL.createObjectURL(blob);
            
            const link = document.createElement('a');
            link.href = url;
            link.download = exported.filename;
            link.style.display = 'none';
            
            document.body.appendChild(link);
            link.click();
            document.body.removeChild(link);
            
            URL.revokeObjectURL(url);
            
            return true;
        } catch (error) {
            console.error('Failed to download theme:', error);
            return false;
        }
    }

    uploadTheme(file) {
        return new Promise((resolve, reject) => {
            const reader = new FileReader();
            
            reader.onload = (e) => {
                try {
                    const content = e.target.result;
                    const format = this.detectFormat(file.name);
                    const theme = this.importTheme(content, format);
                    resolve(theme);
                } catch (error) {
                    reject(error);
                }
            };
            
            reader.onerror = () => {
                reject(new Error('Failed to read file'));
            };
            
            reader.readAsText(file);
        });
    }

    detectFormat(filename) {
        const extension = filename.split('.').pop().toLowerCase();
        
        switch (extension) {
            case 'json':
                return 'json';
            case 'css':
                return 'css';
            case 'scss':
            case 'sass':
                return 'scss';
            default:
                return 'json'; // Default to JSON
        }
    }

    validateTheme(theme) {
        const errors = [];
        
        if (!theme.name || theme.name.trim() === '') {
            errors.push('Theme name is required');
        }
        
        if (!theme.id || theme.id.trim() === '') {
            errors.push('Theme ID is required');
        }
        
        if (!theme.category) {
            errors.push('Theme category is required');
        }
        
        // Validate colors
        if (theme.colors) {
            Object.entries(theme.colors).forEach(([key, value]) => {
                if (!this.isValidColor(value)) {
                    errors.push(`Invalid color value for ${key}: ${value}`);
                }
            });
        }
        
        return {
            isValid: errors.length === 0,
            errors
        };
    }

    isValidColor(color) {
        // Basic color validation
        const colorRegex = /^#([A-Fa-f0-9]{6}|[A-Fa-f0-9]{3})$|^rgb\(\s*\d+\s*,\s*\d+\s*,\s*\d+\s*\)$|^rgba\(\s*\d+\s*,\s*\d+\s*,\s*\d+\s*,\s*[\d.]+\s*\)$|^hsl\(\s*\d+\s*,\s*\d+%\s*,\s*\d+%\s*\)$|^hsla\(\s*\d+\s*,\s*\d+%\s*,\s*\d+%\s*,\s*[\d.]+\s*\)$/;
        return colorRegex.test(color);
    }

    createThemeBackup() {
        const themes = Array.from(this.themeManager.themes.values()).filter(theme => theme.isCustom);
        
        const backup = {
            version: '1.0',
            createdAt: new Date().toISOString(),
            themes: themes,
            metadata: {
                totalThemes: themes.length,
                exportedBy: 'PiXa Prompt Enhancer Theme Editor'
            }
        };
        
        return {
            filename: `theme-backup-${new Date().toISOString().split('T')[0]}.json`,
            content: JSON.stringify(backup, null, 2),
            mimeType: 'application/json'
        };
    }

    restoreThemeBackup(backupContent) {
        try {
            const backup = JSON.parse(backupContent);
            
            if (!backup.themes || !Array.isArray(backup.themes)) {
                throw new Error('Invalid backup format');
            }
            
            const restoredThemes = [];
            
            for (const themeData of backup.themes) {
                try {
                    const theme = this.importTheme(JSON.stringify(themeData), 'json');
                    restoredThemes.push(theme);
                } catch (error) {
                    console.warn(`Failed to restore theme ${themeData.name}:`, error);
                }
            }
            
            return {
                totalRestored: restoredThemes.length,
                themes: restoredThemes
            };
        } catch (error) {
            throw new Error(`Failed to restore backup: ${error.message}`);
        }
    }

    getThemeStatistics() {
        const themes = Array.from(this.themeManager.themes.values());
        
        return {
            totalThemes: themes.length,
            customThemes: themes.filter(theme => theme.isCustom).length,
            defaultThemes: themes.filter(theme => !theme.isCustom).length,
            categories: [...new Set(themes.map(theme => theme.category))],
            mostRecent: themes.sort((a, b) => 
                new Date(b.createdAt || 0) - new Date(a.createdAt || 0)
            ).slice(0, 5)
        };
    }

    // Utility methods
    sanitizeFilename(filename) {
        return filename
            .replace(/[^a-z0-9]/gi, '_')
            .replace(/_+/g, '_')
            .toLowerCase();
    }

    kebabCase(str) {
        return str
            .replace(/([a-z])([A-Z])/g, '$1-$2')
            .replace(/[\s_]+/g, '-')
            .toLowerCase();
    }

    // Public API for file operations
    createFileInput(accept = '.json,.css,.scss') {
        const input = document.createElement('input');
        input.type = 'file';
        input.accept = accept;
        input.style.display = 'none';
        
        return new Promise((resolve) => {
            input.addEventListener('change', (e) => {
                const file = e.target.files[0];
                if (file) {
                    resolve(file);
                } else {
                    resolve(null);
                }
            });
            
            document.body.appendChild(input);
            input.click();
            document.body.removeChild(input);
        });
    }
}

// Initialize theme exporter when DOM is ready
document.addEventListener('DOMContentLoaded', () => {
    setTimeout(() => {
        if (window.themeManager) {
            window.themeExporter = new ThemeExporter(window.themeManager);
        }
    }, 100);
    
    // Make theme exporter globally available
    window.ThemeExporter = ThemeExporter;
});

// Export for module systems
if (typeof module !== 'undefined' && module.exports) {
    module.exports = ThemeExporter;
}
